// Show Main fixed mobile menu
function showMfmm() {
  document.getElementById('fixed-mobile-menu-modal').style.display = "block";
}

// Hide Main fixed mobile menu
function hideMfmm() {
  document.getElementById('fixed-mobile-menu-modal').style.display = "none";
}

